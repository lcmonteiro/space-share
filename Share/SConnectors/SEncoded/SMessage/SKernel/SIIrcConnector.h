class SIIrcConnector : public SInputConnector {
public:
    /**
     * ------------------------------------------------------------------------
     * Constructor
     * ------------------------------------------------------------------------
     */
    SIIrcConnector(const SText& address);
    /**
     * ------------------------------------------------------------------------
     * Get Resource
     * ------------------------------------------------------------------------
     */
    inline Resource& resource() override {
        return __res;
    }
protected:
    /**
     * ------------------------------------------------------------------------
     * IO functions
     * ------------------------------------------------------------------------
     * read
     * ----------------------------------------------------
     */
    Document _read() override;
    /**
     * ------------------------------------------------------------------------
     * control functions
     * ------------------------------------------------------------------------
     * open
     * ----------------------------------------------------
     */
    void _open() override;
    /**
     * ----------------------------------------------------
     * good
     * ----------------------------------------------------
     */
    inline bool _good() override{
        return __res.good();
    }
    /**
     * ----------------------------------------------------
     * close
     * ----------------------------------------------------
     */
    inline void _close() override {
        __res = SIRCResource();
    }
private:
    /**
     * ------------------------------------------------------------------------
     * Variables
     * ------------------------------------------------------------------------
     * resource 
     */
    SIRCResource __res;
};
