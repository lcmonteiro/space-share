#ifndef SBASE64_H
#define SBASE64_H
/**
 * Space
 */
#include "SText.h"
#include "SFrame.h"
/**
 */
namespace SBase64 {
	/**
	 * ------------------------------------------------------------------------
	 * Encode
	 * ------------------------------------------------------------------------
	 */
	SText Encode(Frame::const_iterator begin, Frame::const_iterator end);
	/**
	 * ------------------------------------------------------------------------
	 * Decode
	 * ------------------------------------------------------------------------
	 */
	Frame Decode(SText::const_iterator begin, SText::const_iterator end);
	/**
	 * ------------------------------------------------------------------------
	 * Utilities
	 * ------------------------------------------------------------------------
	 */
	template <typename T>
	SText Encode(const T& data) {
		return Encode(data.begin(), data.end());
	}
	template <typename T>
	Frame Decode(const T& data) {
		return Decode(data.begin(), data.end());
	}
};
/**
 * ------------------------------------------------------------------------------------------------
 * End
 * ------------------------------------------------------------------------------------------------
 */
#endif /* SBASE64_H */

